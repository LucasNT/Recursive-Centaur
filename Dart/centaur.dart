void centaur(int n, bool end) {
  if(n < 0) {
  return;
  }
  centaur(n-1, false);

  //default values
  String top = '|       #';
  String bot = '';
  String space = "";

  if (n == 0) {
    // Prints head.
    top = (" o\n/|\\_____\n|       #");
  }

  else {
    for (int i = 1; i < n; ++i) {
      space += "     ";
    }
    
    top = space + "! !``" + top;
  }
        
  // is last body (legs)?
  if (end) {
      space = "";
      for (int i = 0; i < n; ++i) {
          space += "     ";
      }
      bot = "\n" + space + "! !```! !";
  }
  else {
      top += "____";
  }
    print(top+bot);
}

